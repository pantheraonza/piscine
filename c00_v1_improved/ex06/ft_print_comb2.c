#include <unistd.h>

void	ft_print_comb(void)
{
	int		n1;
	int		n2;
	char	c;

	n1 = 0;
	while (n1 <= 98)
	{
		n2 = n1 + 1;
		while (n2 <= 99)
		{
			c = n1 / 10 + '0';
			write(1, &c, 1);
			c = n1 % 10 + '0';
			write(1, &c, 1);
			write(1, " ", 1);
			c = n2 / 10 + '0';
			write(1, &c, 1);
			c = n2 % 10 + '0';
			write(1, &c, 1);
			if (!(n1 == 98 && n2 == 99))
				write(1, ", ", 2);
			n2++;
		}
		n1++;
	}
}

